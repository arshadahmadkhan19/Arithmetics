import Foundation

public class ArithmeticLib {
    public init() {}
    
    public func add(_ num1: Int, num2: Int) -> Int {
        num1 + num2
    }
    
    public func sub(_ num1: Int, num2: Int) -> Int {
        num1 - num2
    }
}
